import streamlit as st

def main():
    st.title('Web Dashboard')
    
    print('Web Dashboard')
    
    st.text('Develop Webdashboard')

    st.header('This area is header area')

    st.subheader('This area is subheader area')

    st.write('Hello there')

    st.success('When success some action, show text')

    st.warning('warning when have some problem ')

    st.info('show information text')

    st.error('show this text when some error awkard')




if __name__ == '__main__':
    main()
